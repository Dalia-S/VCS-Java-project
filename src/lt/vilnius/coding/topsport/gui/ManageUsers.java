    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try {
            jLabel6.setText("");
            user = jTextField1.getText();
            jLabel2.setText(user);
            String dBQuery = "SELECT password, FirstName, LastName, Address, City, PhoneNumber,"
                    + "BDay, Balance FROM tsusers WHERE UserName = '" + user + "';";
            ResultSet rs = new DBConn().getStmt().executeQuery(dBQuery);
            while (rs.next()) {
                jTable1.setValueAt(rs.getString("password"), 0, 0);
                jTable1.setValueAt(rs.getString("FirstName"), 0, 1);
                jTable1.setValueAt(rs.getString("LastName"), 0, 2);
                jTable1.setValueAt(rs.getString("Address"), 0, 3);
                jTable1.setValueAt(rs.getString("City"), 0, 4);
                jTable1.setValueAt(rs.getString("PhoneNumber"), 0, 5);
                jTable1.setValueAt(rs.getString("BDay"), 0, 6);
                jTable1.setValueAt(rs.getInt("Balance"), 0, 7);
            }
        } catch (ClassNotFoundException | SQLException ex) {
        }
    }//GEN-LAST:event_jButton3ActionPerformed


     private boolean checkDate() {
        String date = String.valueOf(jTable1.getValueAt(0, 6));
        Date testDate = null;
        SimpleDateFormat df = new SimpleDateFormat("yyyy-MM-dd");
        try {
            testDate = df.parse(date);
            if (!df.format(testDate).equals(date)) {
                throw new Exception();
            }
        } catch (Exception e) {
            jLabel6.setText("Please enter date in format YYYY-MM-DD");
            return false;
        }
        return true;
    }
    
